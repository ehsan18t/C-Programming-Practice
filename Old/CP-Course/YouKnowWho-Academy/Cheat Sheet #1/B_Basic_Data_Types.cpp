#include <bits/stdc++.h>

// Opt
#define sync                      \
    ios_base::sync_with_stdio(0); \
    cin.tie(0);                   \
    cout.tie(0);

using namespace std;

#define in_out(var) \
    cin >> var;     \
    cout << var << endl;

// Solution
void solve()
{
    int a;
    in_out(a);
    long long b;
    in_out(b);
    char c;
    in_out(c);
    float d;
    in_out(d);
    double e;
    in_out(e);
}

int main()
{
    sync;
    int n = 1;
    // cin >> n;
    while (n--)
        solve();
    return 0;
}
